package StringPattern;

public class TriangleNumbers {

    public static void main(String[] args) {
        System.out.println("Triangle Numbers pattern: ");
        for (int i=0;i<=5;i++){
            for (int j=1;j<=i;j++){
                System.out.print(j+" ");
            }
            System.out.println();
        }
    }
}
